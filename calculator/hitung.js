module.exports = { plus, minus, multiply, devide, modulus, pow };

function plus(a, b) {
  return a + b;
}

function minus(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b;
}

function devide(a, b) {
  return a / b;
}

function modulus(a, b) {
  return a % b;
}

// pangkat
function pow(a, b) {
  return Math.pow(a, b);
}
